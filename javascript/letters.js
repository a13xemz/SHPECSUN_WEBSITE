
$(document).ready(function() {
    $("letters").hover(function() {
    $("letters img").css("padding-right","0px")
    $("stext").stop(true, false).show("slow").fadeIn("slow");
},function(){
        $("letters").css("padding-right","20px");
        $("stext").stop(true, false).hide("slow").fadeOut("slow");}
                      )});

